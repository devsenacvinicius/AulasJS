// Variaveis

// let é uma palavra reservada para criar variáveis,
// ou seja, um espaço na memória do computador para
// armazenar um valor.

let animal = 'Cachorro'; // declarando a variável animal e atribuindo o valor 'Cachorro'
console.log(animal); // imprimindo o valor da variável "animal" no console

animal = 'Gato'; // reatribuindo o valor da variável "animal" para 'Gato'
console.log(animal); // imprimindo o novo valor da variável "animal" no console

// const é uma palavra reservada para criar constantes,
// ou seja, um espaço na memória do computador para
// armazenar um valor que não pode ser alterado.
const pi = 3.14; // declarando a constante PI e atribuindo o valor 3.14
console.log(pi); // imprimindo o valor da constante "PI" no console

pi = 3.14159; 
console.log(pi);